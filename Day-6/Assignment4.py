#Create a variable total and initialize it to 10.
#Use assignment operators (+=, -=, *=, /=) to update the value of total.
#Print the final value of total.
total = 10

total += 5 # 15 this will use as total for subtraction
print("10+5:", total)
total -= 3 #15-3=12 total for multification
print("15-3:", total)
total *= 2 # 12*2=24 total for division
print("12*2:", total)
total /= 4 # 24/4=6

print("Final total 24/4:", total)
