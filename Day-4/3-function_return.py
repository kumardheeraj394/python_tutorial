def addition(num1, num2):
    add = num1 + num2
    return add

def sub(num1, num2):
    s = num1 - num2
    return s

def mul(num1, num2):
    m = num1 * num2
    return m

def div(num1, num2):
    d = num1 / num2
    return d

# Calling all functions and printing their return values
print("Addition:", addition(6, 10))
print("Subtraction:", sub(10, 5))
print("Multiplication:", mul(5, 10))
